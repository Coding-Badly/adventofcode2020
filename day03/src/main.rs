
struct Slope {
    trees: std::collections::HashSet<(usize, usize)>,
    width: usize,
    height: usize,
}

impl Slope {
    fn from_lines<'a, I, S: Into<&'a str>>(lines: I) -> Slope
        where
            I: Iterator<Item = S>
    {
        let mut trees = std::collections::HashSet::<(usize, usize)>::new();
        let mut width = 0;
        let mut height = 0;
        for (row, line) in lines.enumerate() {
            let line = line.into();
            if line.len() > width {
                width = line.len();
            }
            for (column, rover) in line.chars().enumerate() {
                if rover == '#' {
                    trees.insert((column, row));
                }
            }
            height += 1;
        }
        Slope {
            trees,
            width,
            height,
        }
    }
    fn is_tree(&self, column: usize, row: usize) -> Option<bool> {
        if row < self.height {
            let column = column % self.width;
            Some(self.trees.contains(&(column, row)))
        }
        else {
            None
        }
    }
    fn how_many_trees(&self, column_delta: usize, row_delta: usize) -> usize {
        let mut c = 0;
        let mut r = 0;
        let mut trees = 0;

        while let Some(result) = self.is_tree(c, r) {
            if result {
                trees += 1;
            }
            c += column_delta;
            r += row_delta;
        }
        trees
    }
}

struct SlopeRunner {
    slope: Slope,
    tree_counts: Vec<usize>,
}

impl SlopeRunner {
    fn new(slope: Slope) -> Self {
        Self {
            slope,
            tree_counts: Vec::new(),
        }
    }
    fn run(&mut self, column_delta: usize, row_delta: usize) {
        self.tree_counts.push(self.slope.how_many_trees(column_delta, row_delta));
    }
    fn result(&self) -> usize {
        self.tree_counts.iter().fold(1, |accumulator, value| accumulator * value)
    }
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let slope = Slope::from_lines(INPUT.lines()
        .map(|line| line
        .trim())
        .filter(|line| line.len() > 0));
    let mut runner = SlopeRunner::new(slope);
    runner.run(1, 1);
    runner.run(3, 1);
    runner.run(5, 1);
    runner.run(7, 1);
    runner.run(1, 2);
    println!("{}", runner.result());

    Ok(())
}

